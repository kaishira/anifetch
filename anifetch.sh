IFS=$'\n' read -r -d '' -a info_array <<< "$text"
IFS=$'\n' read -r -d '' -a art_array <<< "$art"

max_lines=${#info_array[@]}
if [ ${#art_array[@]} -gt $max_lines ]; then
	    max_lines=${#art_array[@]}
fi

for ((i=0; i<max_lines; i++)); do
	    info_line="${info_array[i]}"
	        art_line="${art_array[i]}"
		    printf "%-40s %s\n" "$art_line" "$info_line"
	    done

echo ""
